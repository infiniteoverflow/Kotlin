fun main(args: Array<String>) {
    var age = 19
    var name: String = "Aswin"

    println("My name is $name and i am $age years old")
}

/* Here the keyword 'var' is used to declare a variable. It takes on the type of the data provided to
   it.
   We can explicitly define the type of the variable by using a ':' followed by the type of the
   variable.

   A variable can be accessed in a println() using the '$' symbol followed by the variable name.
 */